const initialLang = supportedLangs.includes(deviceLang) ? deviceLang : 'ru';

i18n.use(initReactI18next).init({
  resources,
  lng: initialLang,
  fallbackLng: 'ru',
  interpolation: { escapeValue: false },
  compatibilityJSON: 'v4',
});

export default i18n;
